const LEGACY_EN_PATTERNS: PatternTranslator[] = [
  {
    pattern: /^(\d+) activite\(s\)$/,
    translate: (count) => `${count} activit${count === '1' ? 'y' : 'ies'}`,
  },
  {
    pattern: /^(\d+) resultat\(s\)$/,
    translate: (count) => `${count} result${count === '1' ? '' : 's'}`,
  },
  {
    pattern: /^(\d+) enfant\(s\) trouve\(s\)$/,
    translate: (count) => `${count} child${count === '1' ? '' : 'ren'} found`,
  },
  {
    pattern: /^(\d+) enfant\(s\) inscrit\(s\)$/,
    translate: (count) => `${count} enrolled child${count === '1' ? '' : 'ren'}`,
  },
  {
    pattern: /^(\d+) parent\(s\), (\d+) enfant\(s\)$/,
    translate: (parents, children) =>
      `${parents} parent${parents === '1' ? '' : 's'}, ${children} child${
        children === '1' ? '' : 'ren'
      }`,
  },
  {
    pattern: /^(\d+) notification\(s\) non lue\(s\)$/,
    translate: (count) => `${count} unread notification${count === '1' ? '' : 's'}`,
  },
  {
    pattern: /^(\d+) soumission\(s\) recueillie\(s\)\.$/,
    translate: (count) => `${count} submission${count === '1' ? '' : 's'} collected.`,
  },
  {
    pattern: /^(\d+) soumission\(s\) de quiz utilisees pour situer la difficulte\.$/,
    translate: (count) =>
      `${count} quiz submission${count === '1' ? '' : 's'} used to assess difficulty.`,
  },
  {
    pattern: /^(\d+) fiche\(s\) • page (\d+)\/(\d+)$/,
    translate: (count, current, total) => `${count} guide(s) • page ${current}/${total}`,
  },
  {
    pattern: /^Question (\d+) \/ (\d+)$/,
    translate: (current, total) => `Question ${current} / ${total}`,
  }
];

@Injectable({ providedIn: 'root' })
export class LegacyI18nService {
  translate(text: string, language: AppLanguage): string {
    if (language === 'fr') {
      return text;
    }

    const exact = LEGACY_EN_TRANSLATIONS[text];
    if (exact) {
      return exact;
    }

    for (const rule of LEGACY_EN_PATTERNS) {
      const match = text.match(rule.pattern);
      if (match) {
        return rule.translate(...match.slice(1));
      }
    }

    return text;
  }
}
